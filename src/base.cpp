#include <math.h>

#include "base.h"

namespace Base {

//------------------------------------------------------------------------------

unit Vec2d::length() const
{
	return sqrt(x * x + y * y);
}

//------------------------------------------------------------------------------

unit Vec2d::lengthM() const
{
	return abs(x + y);
}

//------------------------------------------------------------------------------

unit Vec2d::angle() const
{
	return atan2(y, x);
}

//------------------------------------------------------------------------------

Vec2d Vec2d::fromAngle(unit a)
{
	return Vec2d(cos(a), sin(a));
}

//------------------------------------------------------------------------------

} /* namespace Base */

